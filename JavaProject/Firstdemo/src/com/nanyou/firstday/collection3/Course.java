package com.nanyou.firstday.collection3;

public class Course {
    private int cid;
    private String cname;

    public Course() {//无参构造器
    }

    public Course(int cid, String cname) {//有参构造器
        this.cid = cid;
        this.cname = cname;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }

    @Override
    public String toString() {
        return "Course{" +
                "cid=" + cid +
                ", cname='" + cname + '\'' +
                '}';
    }
}
